package pack;

public class LeftshiftDemo {
	public static void main(String[]args) {
		int a=5;
		int b=5;
		int c=a<<2;
		System.out.println(c);
		int d=a>>3;
		System.out.println(d);
		System.out.println(11>>>2);
		System.out.println(-11>>>2);
		int t;
		t=a&b;
		System.out.println(t);
	}
}